#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - The sum of two integers
 * @a: An integer
 * @b: An integer
 *
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - The difference between two integers
 * @a: An integer
 * @b: An integer
 *
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - The multiplication of two integers
 * @a: An integer
 * @b: An integer
 *
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - The division of two integers
 * @a: An integer
 * @b: An integer
 *
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - The remainder of the division
 * @a: An integer
 * @b: An integer
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
